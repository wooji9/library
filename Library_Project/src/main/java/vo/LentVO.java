package vo;

public class LentVO {
	private int idx;
	private String m_id;
	private String b_isbl;
	private String lent;
	private String lentdate;
	private String redate;
	private String b_name;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	public String getB_isbl() {
		return b_isbl;
	}
	public void setB_isbl(String b_isbl) {
		this.b_isbl = b_isbl;
	}
	public String getLent() {
		return lent;
	}
	public void setLent(String lent) {
		this.lent = lent;
	}
	public String getLentdate() {
		return lentdate;
	}
	public void setLentdate(String lentdate) {
		this.lentdate = lentdate;
	}
	public String getRedate() {
		return redate;
	}
	public void setRedate(String redate) {
		this.redate = redate;
	}
	public String getB_name() {
		return b_name;
	}
	public void setB_name(String b_name) {
		this.b_name = b_name;
	}
	
}
